import { TAXONOMY_VERSION } from '../../taxonomy/contentTaxonomy.js'

const baseContent = Object.freeze({ status: 'published', version: 1, revision: 1 })
const baseSource = Object.freeze({
  kind: 'ai-generated',
  name: 'Study Website',
  externalId: null,
  license: null,
})

function multipleChoice({ id, domainId, skillId, prompt, options, correctOptionId, explanation, tags = [] }) {
  return {
    id,
    taxonomy: {
      version: TAXONOMY_VERSION,
      examId: 'sat',
      subjectId: 'sat-math',
      domainId,
      skillId,
      questionTypeId: 'multiple-choice',
      answeringMethodId: 'select-option',
    },
    renderer: 'multiple-choice',
    prompt,
    answer: { kind: 'selected-response', options, correctOptionId },
    explanation,
    source: { ...baseSource },
    content: { ...baseContent },
    tags,
  }
}

function studentProducedResponse({ id, domainId, skillId, prompt, acceptedAnswers, explanation, tags = [] }) {
  return {
    id,
    taxonomy: {
      version: TAXONOMY_VERSION,
      examId: 'sat',
      subjectId: 'sat-math',
      domainId,
      skillId,
      questionTypeId: 'student-produced-response',
      answeringMethodId: 'enter-answer',
    },
    renderer: 'grid-in',
    prompt,
    answer: { kind: 'student-produced-response', acceptedAnswers, matching: 'exact-trimmed' },
    explanation,
    source: { ...baseSource },
    content: { ...baseContent },
    tags,
  }
}

